package com.example.finnkinoapp.ui.Movie;

public class Theatre {
    private int ID;
    private String name;

    public void setID(int id){
        this.ID = id;
    }

    public void setName(String nme){
        this.name = nme;
    }

    public int getID() {
        return(this.ID);
    }

    public String getName() {
        return(this.name);
    }
}
